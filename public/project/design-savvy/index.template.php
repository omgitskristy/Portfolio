		<h2>Culture Club (5 o'clock teenager)</h2><br/>
		<h3>Jared Nickerson</h3><br/><br/>
		<p>This was a promo job for Culture Club in Belgium. The type had to read "DO YOU KNOW WHERE YOUR TEENAGER IS AT 5'OCLOCK IN THE MORNING?" The
        designer got to choose the style of the piece as long as the copy was included. Jared Nickerson used a dark orange background with a pop of teal to 
        outline the text that he cleverly laid out. The best part of this piece is the little creature in the right bottom hand corner with a huge smile. 
        This piece is definitely effective.
